# Módulos são similares às Classes porque armazenam
# métodos, constantes e até classes.

# Diferente das classes você não pode instanciar os módulos
# Também não é permitido fazer herança de módulos

# Módulos serve para organizar constantes num lugar centralizado
# Módulos agem como namespace, não conflita com nomes que já estão no seu sistema

# Permite que compartilhe funcionalidade entre classes
# Pode gerar mixins, mistura de classes entre módulos
# Esses mixins permitem uma pseudo herança múltipla
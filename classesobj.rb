# Classes são as fôrmas e aos objetos são os objetos prontos

# Quando se constrói uma casa, você pode ter a planta (fôrma/classe)
# Mas você pde construir várias coisas com a mesma fôrma, que são os objetos
# O objeto é quando se dá vida à classe

# A classe são a maneira de como moldar como o objeto funcione
# A partir da classe pode se moldar que métodos e atributos possuirão
# Métodos = Ações
# Atributos = Características

=begin
ex:
M e A dos carros:
